// Pipeline stats computed from data
export function getPipelineStats() {
  const total = jobs.length;
  const active = jobs.filter(j => !["rejected", "passed"].includes(j.status)).length;
  const interviewing = jobs.filter(j => j.status === "interviewing").length;
  const rejected = jobs.filter(j => j.status === "rejected").length;
  const newOpps = jobs.filter(j => j.status === "new_opportunity").length;
  const applied = jobs.filter(j => ["applied", "app_viewed", "follow_up_needed"].includes(j.status)).length;
  const avgScore = Math.round(jobs.reduce((sum, j) => sum + j.overallScore, 0) / total);
  const topGrade = jobs.sort((a, b) => b.overallScore - a.overallScore)[0];

  return { total, active, interviewing, rejected, newOpps, applied, avgScore, topGrade: topGrade.company };
}
